using namespace std;

enum class Lang {
  DE, FR, IT
};

struct Region {
  string std_name;
  string parent_std_name;
  map<Lang, string> names;
  int64_t population;
};

std::ostream& operator <<(std::ostream& out, Lang lang) {
  switch(lang) {
  case Lang::DE:
    out << "DE";
    break;
  case Lang::FR:
    out << "FR";
    break;
  case Lang::IT:
    out << "IT";
    break;
  default:
    out << "??";
  }
  return out;
}

std::ostream& operator <<(std::ostream& out, const Region& region) {
  return out << '<' << region.std_name << ' ' << region.parent_std_name << ' ' << region.names << ' ' << region.population << '>';
}

